def write_if_missing(path: Path, content: str) -> None:
    if not path.exists():
        path.write_text(content, encoding="utf-8")


def read_state() -> dict:
    if not STATE_PATH.exists():
        return {}
    data = {}
    for line in STATE_PATH.read_text(encoding="utf-8").splitlines():
        if ":" in line and not line.startswith("#"):
            key, value = line.split(":", 1)
            data[key.strip()] = value.strip()
    return data


def write_state(state: dict) -> None:
    STATE_PATH.write_text(
        "# STATE\n\n" + "\n".join(f"{k}: {v}" for k, v in state.items()) + "\n",
        encoding="utf-8",
    )


def update_project_locked(locked: bool) -> None:
    if not PROJECT_PATH.exists():
        return
    text = PROJECT_PATH.read_text(encoding="utf-8")
    text = re.sub(r"- Locked: .*", f"- Locked: {'true' if locked else 'false'}", text, flags=re.IGNORECASE)
    text = re.sub(r"- Last Updated: .*", f"- Last Updated: {TODAY}", text, flags=re.IGNORECASE)
    PROJECT_PATH.write_text(text, encoding="utf-8")


def parse_slices(text: str) -> list[dict]:
    """Parse SLICES.md into a list of slice dicts.

    Each dict contains:
      id          str   e.g. "S1"
      title       str   e.g. "S1 - Initialize Repo Docs"
      status      str   pending | in_progress | done | failed
      burst       str   e.g. "M1.B1"
      description str
      inputs      str
      outputs     str
      contracts   list[str]  relative paths to contract files
      tests       str
      body        str   full raw block text (all lines including heading)
    """
    slices: list[dict] = []
    current_heading: str | None = None
    current_lines: list[str] = []

    def flush(heading: str, lines: list[str]) -> None:
        body = "\n".join(lines).strip()
        # Extract id from heading: "S1 - Name" -> "S1"
        id_match = re.match(r"(S\d+)", heading)
        slice_id = id_match.group(1) if id_match else heading.split()[0]

        fields: dict[str, str] = {}
        for line in lines:
            # Match both plain "- Field: value" and bold "- **Field:** value" styles
            # \s* (not \s+) tolerates missing space after hyphen (PM formatting errors)
            m = re.match(r"-\s*\*{0,2}(\w[\w\s]*?)\*{0,2}:\s*(.*)", line)
            if m:
                fields[m.group(1).strip().lower()] = m.group(2).strip()

        contracts_raw = fields.get("contracts", "")
        contracts = [c.strip() for c in contracts_raw.split(",") if c.strip()] if contracts_raw else []

        slices.append({
            "id": slice_id,
            "title": heading,
            "status": fields.get("status", "pending"),
            "burst": fields.get("burst", ""),
            "description": fields.get("description", ""),
            "inputs": fields.get("inputs", ""),
            "outputs": fields.get("outputs", ""),
            "contracts": contracts,
            "tests": fields.get("tests", ""),
            "body": body,
        })

    for line in text.splitlines():
        if line.startswith("## "):
            if current_heading is not None:
                flush(current_heading, current_lines)
            current_heading = line.replace("## ", "", 1).strip()
            current_lines = [line]
        elif current_heading is not None:
            current_lines.append(line)

    if current_heading is not None:
        flush(current_heading, current_lines)

    return slices


def load_slices() -> list[dict]:
    """Load slices from SLICES.md with sidecar status overrides applied.

    Sidecar files in .forger/status/[id] take precedence over SLICES.md status.
    This allows concurrent agents to update per-slice status without contending
    on the shared SLICES.md file.
    """
    if not SLICES_PATH.exists():
        return []
    slices = parse_slices(SLICES_PATH.read_text(encoding="utf-8"))
    if STATUS_DIR.exists():
        for s in slices:
            sidecar = STATUS_DIR / s["id"]
            if sidecar.exists():
                s["status"] = sidecar.read_text(encoding="utf-8").strip()
    return slices


def update_slice_status(slice_id: str, new_status: str) -> bool:
    """Update the Status field of a specific slice in SLICES.md and its sidecar file.

    The sidecar file (.forger/status/[id]) is written first -- it is the
    authoritative status source for concurrent execution. SLICES.md is also
    updated for human readability and tools that read it directly.

    Returns True if the slice was found and updated, False otherwise.
    """
    # Write sidecar file first (atomic single-file write, no shared-file contention)
    STATUS_DIR.mkdir(exist_ok=True)
    (STATUS_DIR / slice_id).write_text(new_status, encoding="utf-8")

    # Also update SLICES.md for human readability
    if not SLICES_PATH.exists():
        return False

    text = SLICES_PATH.read_text(encoding="utf-8")
    lines = text.splitlines()
    result_lines: list[str] = []

    in_target = False
    updated = False

    for line in lines:
        if line.startswith("## "):
            # Check if this heading matches our target slice
            heading = line.replace("## ", "", 1).strip()
            id_match = re.match(r"(S\d+)", heading)
            found_id = id_match.group(1) if id_match else heading.split()[0]
            in_target = (found_id == slice_id)

        if in_target and re.match(r"-\s*\*{0,2}Status\*{0,2}:\s*.*", line, re.IGNORECASE):
            result_lines.append(f"- Status: {new_status}")
            updated = True
        else:
            result_lines.append(line)

    if updated:
        SLICES_PATH.write_text("\n".join(result_lines) + "\n", encoding="utf-8")

    return updated


_REQUIRED_SLICE_FIELDS = ("burst", "status", "description", "inputs", "outputs", "tests")
_VALID_STATUSES = {"pending", "in_progress", "done", "failed"}


def validate_slices(slices: list[dict]) -> list[str]:
    """Validate parsed slices and return a list of warning strings.

    Checks required fields, valid status values, and contract presence.
    Call this after parse_slices() to surface PM formatting errors early.
    """
    warnings: list[str] = []
    for s in slices:
        sid = s["id"]
        for field in _REQUIRED_SLICE_FIELDS:
            if not s.get(field):
                warnings.append(f"{sid}: missing or empty field '{field}'")
        status = s.get("status", "")
        if status and status not in _VALID_STATUSES:
            warnings.append(f"{sid}: invalid status '{status}' (valid: {', '.join(sorted(_VALID_STATUSES))})")
        if not s.get("contracts"):
            warnings.append(f"{sid}: no contracts listed -- DEV will have no specification")
    return warnings


def _parse_file_list(raw: str) -> list[str]:
    """Parse a comma-separated list of file paths from a slice Inputs or Outputs field.

    Filters out:
    - URLs (http/https references -- these are documentation, not dependency files)
    - Parenthetical annotations like "(updated)", "(modified)" -- PM style artifacts
    - Empty strings

    Example:
        "Dockerfile, scripts/install.sh (updated), https://example.com"
        -> ["Dockerfile", "scripts/install.sh"]
    """
    items = []
    for part in raw.split(","):
        part = part.strip()
        # Strip trailing parenthetical annotations (e.g. "(updated)", "(modified)")
        part = re.sub(r"\s*\([^)]*\)\s*$", "", part).strip()
        # Skip URLs and empty strings
        if part and not part.lower().startswith("http"):
            items.append(part)
    return items


def build_dependency_graph(slices: list[dict]) -> dict[str, set[str]]:
    """Build a dependency adjacency map: slice_id -> set of slice_ids it depends on.

    Dependency is inferred from Inputs/Outputs overlap:
    If slice B lists a file in its Inputs that slice A lists in its Outputs,
    then B depends on A (A must complete before B can start).

    When multiple slices produce the same file (incremental update pattern, e.g.
    S1 creates Dockerfile, S2 updates it, S3 updates it again), a slice depends on
    the nearest prior producer -- not the last global producer. This prevents false
    cycles from backward-in-time dependencies.

    Only file-based dependencies between slices are captured -- external files,
    URLs, and pre-existing project files are not tracked.
    """
    # Build ordered producer list: file -> [(slice_index, slice_id), ...]
    output_producers: dict[str, list[tuple[int, str]]] = {}
    for idx, s in enumerate(slices):
        for out_file in _parse_file_list(s["outputs"]):
            if out_file not in output_producers:
                output_producers[out_file] = []
            output_producers[out_file].append((idx, s["id"]))

    # slice_id -> index in the slices list (for ordering)
    slice_index = {s["id"]: idx for idx, s in enumerate(slices)}

    # For each slice, depend on the nearest prior producer of each input file
    deps: dict[str, set[str]] = {s["id"]: set() for s in slices}
    for s in slices:
        curr_idx = slice_index[s["id"]]
        for inp_file in _parse_file_list(s["inputs"]):
            producers = output_producers.get(inp_file, [])
            # Only consider producers that appear BEFORE this slice (lower index)
            prior = [(idx, sid) for idx, sid in producers if idx < curr_idx]
            if prior:
                # Depend on the most recent prior producer (highest index < current)
                _, nearest = max(prior, key=lambda x: x[0])
                deps[s["id"]].add(nearest)

    return deps


def detect_cycles(deps: dict[str, set[str]]) -> list[str]:
    """Detect cycles in the dependency graph using Kahn's algorithm.

    Returns a list of slice IDs involved in cycles (empty list = no cycles).
    A non-empty return means the dependency graph is not a DAG and cannot
    be executed in any valid order.
    """
    # in_degree[node] = number of slices this node depends on
    in_degree = {node: len(node_deps) for node, node_deps in deps.items()}

    # Process nodes with no remaining dependencies
    queue = [node for node, deg in in_degree.items() if deg == 0]
    processed = 0

    while queue:
        node = queue.pop(0)
        processed += 1
        # Reduce in_degree for all nodes that depended on this node
        for other, other_deps in deps.items():
            if node in other_deps:
                in_degree[other] -= 1
                if in_degree[other] == 0:
                    queue.append(other)

    # Any nodes not processed are part of a cycle
    return [node for node, deg in in_degree.items() if deg > 0]


def get_ready_slices(slices: list[dict]) -> list[dict]:
    """Return all pending slices whose input file dependencies are fully satisfied.

    A slice is "ready" if every file in its Inputs that is produced by another
    slice has already been marked done. External files (requirements.txt, config
    files not produced by any slice, URLs) are always treated as available.

    This is the core of the wave-based execution model: call this after each
    slice completes to discover newly unblocked work.
    """
    # Files produced by completed slices
    done_outputs: set[str] = set()
    for s in slices:
        if s["status"] == "done":
            for f in _parse_file_list(s["outputs"]):
                done_outputs.add(f)

    # All files produced by any slice (to distinguish slice-produced from external)
    all_slice_outputs: set[str] = set()
    for s in slices:
        for f in _parse_file_list(s["outputs"]):
            all_slice_outputs.add(f)

    ready = []
    for s in slices:
        if s["status"] != "pending":
            continue
        # Blocked if any input is slice-produced but not yet done
        blocked = any(
            inp in all_slice_outputs and inp not in done_outputs
            for inp in _parse_file_list(s["inputs"])
        )
        if not blocked:
            ready.append(s)

    return ready


def embed_contract(contract_path: str) -> str:
    """Read a contract file and return its content for embedding in a task packet.

    Contract paths must be fully qualified from project root (e.g., '.forger/contracts/foo.md').
    Enforces this requirement to prevent agents from constructing incorrect paths.
    """
    # Enforce fully-qualified path from project root
    if not contract_path.startswith(".forger/contracts/"):
        print(f"ERROR: Contract path must start with '.forger/contracts/', got: {contract_path}", file=sys.stderr)
        print(f"       Contracts must be listed in SLICES.md with full paths (e.g., '.forger/contracts/users_service.md')", file=sys.stderr)
        return f"[Contract path error: {contract_path} -- must start with '.forger/contracts/']"

    path = ROOT / contract_path
    if path.exists():
        return path.read_text(encoding="utf-8")

    print(f"ERROR: contract file not found: {path}", file=sys.stderr)
    return f"[Contract file not found: {contract_path}]"


def cmd_init(_: argparse.Namespace) -> None:
    # Warn if old-style forger/ directory (pre-.forger/ naming) exists -- it confuses agents
    old_forger = ROOT / "forger"
    if old_forger.exists() and old_forger.is_dir():
        print("WARNING: Found old-style 'forger/' directory.")
        print("  This conflicts with the new '.forger/' naming and will confuse agents.")
        print("  Delete it before running Forger: rmdir /s /q forger  (or rm -rf forger)")
        print()

    # Create .forger/ subdirectory -- all workflow files live here
    FORGER_DIR.mkdir(exist_ok=True)

    write_if_missing(STATE_PATH, STATE_TEMPLATE.format(date=TODAY))
    write_if_missing(PROJECT_PATH, PROJECT_TEMPLATE.format(date=TODAY))
    write_if_missing(MILESTONES_PATH, MILESTONES_TEMPLATE)
    write_if_missing(BURSTS_PATH, BURSTS_TEMPLATE)
    write_if_missing(SLICES_PATH, SLICES_TEMPLATE)
    CONTRACTS_DIR.mkdir(exist_ok=True)
    TASKS_DIR.mkdir(exist_ok=True)
    QA_REPORTS_DIR.mkdir(exist_ok=True)
    STATUS_DIR.mkdir(exist_ok=True)
    (FORGER_DIR / "templates").mkdir(exist_ok=True)
    write_if_missing(CONTRACTS_DIR / "CONTRACT_TEMPLATE.md", CONTRACT_TEMPLATE)
    write_if_missing(FORGER_DIR / "templates" / "TASK_PACKET_TEMPLATE.md", TASK_PACKET_TEMPLATE)

    # Copy forger_cli.py to project root so agents run it without any path prefix
    src_cli = FORGER_CLI
    dst_cli = ROOT / "forger_cli.py"
    if src_cli.resolve() != dst_cli.resolve() and not dst_cli.exists():
        shutil.copy2(src_cli, dst_cli)
        print("CLI copied: forger_cli.py")
    elif dst_cli.exists():
        print("CLI already present: forger_cli.py")

    # Install Kilo Code modes to this project (.kilocodemodes at project root)
    modes_src = FORGER_ROOT / "custom_modes.yaml"
    kilocodemodes = ROOT / ".kilocodemodes"
    if modes_src.exists() and not kilocodemodes.exists():
        shutil.copy2(modes_src, kilocodemodes)
        print("Kilo Code modes installed: .kilocodemodes")
    elif kilocodemodes.exists():
        print("Kilo Code modes already present: .kilocodemodes")
    else:
        print(f"Warning: modes source not found at {modes_src}")

    # Copy prompts/ to forger/ so spawned agents can read role definitions
    prompts_src = FORGER_ROOT / "prompts"
    prompts_dst = FORGER_DIR / "prompts"
    if prompts_src.exists() and prompts_src.resolve() != prompts_dst.resolve():
        if not prompts_dst.exists():
            shutil.copytree(prompts_src, prompts_dst)
            print("Agent prompts copied: .forger/prompts/")
        else:
            print("Agent prompts already present: .forger/prompts/")

    print(f"\nInitialized project at: {ROOT}")
    print("Workflow files: .forger/PROJECT.md, .forger/STATE.md, .forger/SLICES.md, ...")
    print("Directories:    .forger/contracts/, .forger/tasks/, .forger/qa_reports/, .forger/status/")
    print("Root files:     forger_cli.py, .kilocodemodes")
    print()
    print("Next steps:")
    print("  1. Open this project in VSCode + Kilo Code")
    print("  2. Turn YOLO OFF, switch to 'forger-planner' mode, have the discovery conversation")
    print("  3. Say 'locked' when satisfied with the project definition")
    print("  4. After PM presents the planning summary and you confirm, turn YOLO ON")
    print("  5. The Orchestrator runs all slices autonomously to completion")


def cmd_install(_: argparse.Namespace) -> None:
    """Install Forger modes globally for all Kilo Code projects."""
    modes_src = FORGER_ROOT / "custom_modes.yaml"
    if not modes_src.exists():
        print(f"Error: modes file not found at {modes_src}")
        return
    kilo_dir = Path.home() / ".kilo"
    kilo_dir.mkdir(exist_ok=True)
    dest = kilo_dir / "custom_modes.yaml"
    shutil.copy2(modes_src, dest)
    print(f"Forger modes installed globally: {dest}")
    print("Modes will be available in all Kilo Code projects without running 'init'.")


def cmd_status(_: argparse.Namespace) -> None:
    state = read_state()
    if not state:
        print("No STATE.md found. Run 'init' first.")
        return

    phase = state.get("phase", "DISCOVERY")
    locked = state.get("locked", "false")

    print(f"Phase:      {phase}")
    print(f"Locked:     {locked}")
    print(f"Locked At:  {state.get('locked_at', '-')}")
    print(f"Updated:    {state.get('last_updated', '-')}")
    print(f"Interrupt:  {state.get('interrupt_mode', 'autonomous')}")

    slices = load_slices()
    if slices:
        counts: dict[str, int] = {}
        for s in slices:
            counts[s["status"]] = counts.get(s["status"], 0) + 1
        total = len(slices)
        done = counts.get("done", 0)
        print(f"\nSlices:     {done}/{total} done")
        for status, count in sorted(counts.items()):
            print(f"  {status}: {count}")
        issues = validate_slices(slices)
        if issues:
            print(f"\nSlice validation warnings ({len(issues)}):")
            for w in issues:
                print(f"  * {w}")

    # Next-action hint
    print()
    if locked != "true":
        print("Next:  Switch to 'forger-planner' mode -> have the discovery conversation -> say 'locked'")
    elif phase == "DISCOVERY":
        print("Next:  Run 'forger plan' or switch to 'forger-pm' mode to begin planning")
    elif phase == "PLANNING":
        print("Next:  PM agent is generating planning docs -- wait for its confirmation dialog")
    elif phase == "EXECUTING":
        task_packets = list(TASKS_DIR.glob("S*.md")) if TASKS_DIR.exists() else []
        task_packets = [p for p in task_packets if "-DONE" not in p.name and "-DEBUG" not in p.name]
        if not task_packets:
            print("Next:  Run 'forger orchestrate' to generate task packets")
        else:
            pending = [s for s in slices if s["status"] == "pending"]
            in_prog = [s for s in slices if s["status"] == "in_progress"]
            done_sl = [s for s in slices if s["status"] == "done"]
            failed = [s for s in slices if s["status"] == "failed"]
            if pending or in_prog:
                print("Next:  Switch to 'forger-orchestrator' mode -> execution will begin automatically")
                print("       Tip:  Turn YOLO ON before switching to Orchestrator for unattended execution")
            elif len(done_sl) + len(failed) == len(slices) and slices:
                print("Next:  Switch to 'forger-auditor' mode for the final compliance audit")


def cmd_lock(_: argparse.Namespace) -> None:
    state = read_state()
    if not state:
        print("No STATE.md found. Run 'init' first.")
        return
    state["locked"] = "true"
    state["locked_at"] = TODAY
    state["last_updated"] = TODAY
    write_state(state)
    update_project_locked(True)
    print("Project locked.")


def cmd_unlock(_: argparse.Namespace) -> None:
    state = read_state()
    if not state:
        print("No STATE.md found. Run 'init' first.")
        return
    state["locked"] = "false"
    state["locked_at"] = ""
    state["last_updated"] = TODAY
    write_state(state)
    update_project_locked(False)
    print("Project unlocked.")


def cmd_reset(_: argparse.Namespace) -> None:
    """Reset execution state back to PLANNING phase for replanning.

    Clears: .forger/tasks/, .forger/qa_reports/, .forger/status/, SLICES.md, MILESTONES.md, BURSTS.md
    Preserves: PROJECT.md (locked), STATE.md (locked=true), contracts/
    """
    state = read_state()
    if not state:
        print("No STATE.md found. Run 'init' first.")
        return
    if state.get("locked") != "true":
        print("Project is not locked. Nothing to reset (use 'init' to start fresh).")
        return

    # Wipe execution artifacts
    wiped = 0
    for d in (TASKS_DIR, QA_REPORTS_DIR):
        if d.exists():
            for f in d.glob("*.md"):
                f.unlink()
                wiped += 1

    # Wipe sidecar status files
    status_wiped = 0
    if STATUS_DIR.exists():
        for f in STATUS_DIR.iterdir():
            if f.is_file():
                f.unlink()
                status_wiped += 1

    # Reset planning docs to blank templates
    SLICES_PATH.write_text(SLICES_TEMPLATE, encoding="utf-8")
    MILESTONES_PATH.write_text(MILESTONES_TEMPLATE, encoding="utf-8")
    BURSTS_PATH.write_text(BURSTS_TEMPLATE, encoding="utf-8")

    # Reset phase to PLANNING (project stays locked)
    state["phase"] = "PLANNING"
    state["last_updated"] = TODAY
    write_state(state)

    print("Project reset to PLANNING phase.")
    print(f"  Cleared:   {wiped} task/QA report file(s) from .forger/tasks/ and .forger/qa_reports/")
    if status_wiped:
        print(f"  Cleared:   {status_wiped} sidecar status file(s) from .forger/status/")
    print("  Reset:     .forger/SLICES.md, .forger/MILESTONES.md, .forger/BURSTS.md (blank templates)")
    print("  Preserved: .forger/PROJECT.md (locked), .forger/contracts/, STATE locked=true")
    print()
    print("Next:  Switch to 'forger-pm' mode to regenerate the planning documents.")


