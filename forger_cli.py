def cmd_orchestrate(_: argparse.Namespace) -> None:
    if not SLICES_PATH.exists():
        print("No SLICES.md found. Run 'plan' first.")
        return

    slices_text = SLICES_PATH.read_text(encoding="utf-8")
    slices = parse_slices(slices_text)
    if not slices:
        print("No slices found in SLICES.md.")
        return

    # Validate slice format before generating packets
    issues = validate_slices(slices)
    if issues:
        print("SLICES.md validation warnings -- PM must fix these before execution:")
        for w in issues:
            print(f"  * {w}", file=sys.stderr)
        print()

    # Build dependency graph and detect cycles before generating packets
    deps = build_dependency_graph(slices)
    cycles = detect_cycles(deps)
    if cycles:
        print("Error: circular dependencies detected in SLICES.md Inputs/Outputs:", file=sys.stderr)
        for cycle_id in cycles:
            # Find the inputs that create the cycle for this node
            cycle_deps = sorted(deps.get(cycle_id, set()))
            print(f"  {cycle_id} <-> {', '.join(cycle_deps)}", file=sys.stderr)
        print("Fix the circular Inputs/Outputs references before running orchestrate.", file=sys.stderr)
        print("Each slice's Inputs must only reference files produced by earlier slices.", file=sys.stderr)
        return

    TASKS_DIR.mkdir(exist_ok=True)
    STATUS_DIR.mkdir(exist_ok=True)
    created = 0

    for s in slices:
        slice_id = s["id"]
        task_path = TASKS_DIR / f"{slice_id}.md"

        # Embed contract contents
        contracts_section = ""
        if s["contracts"]:
            contract_parts = []
            for cp in s["contracts"]:
                content = embed_contract(cp)
                contract_parts.append(f"### Contract: {cp}\n\n{content}")
            contracts_section = "\n\n".join(contract_parts)
        else:
            print(f"Warning: slice {slice_id} has no contracts listed -- DEV agent will have no specification", file=sys.stderr)
            contracts_section = "(no contracts defined -- PM must add contracts before execution)"

        tests_section = s["tests"] if s["tests"] else "(no tests specified)"
        packet = f"""# TASK PACKET - {s["title"]}

## Slice Definition

{s["body"]}

## Contracts

{contracts_section}

## Implementation Instructions
- Read this packet fully before writing any code.
- Implement only the files described in the Contracts above.
- Do not touch locked documents (.forger/PROJECT.md, .forger/STATE.md).
- Run the tests listed below after implementation. Tests MUST run and produce output. "No tests ran" is a failure.
- Do NOT create fallback/simulation implementations. If a real integration (database, API, external service) is required, implement it genuinely. Hardcoded stub values are not acceptable.
- Write a concise completion summary to: `.forger/tasks/{slice_id}-DONE.md` (include actual test output)
- DO NOT run `python forger_cli.py complete-slice` -- the Orchestrator will mark the slice done after QA passes.

## Tests

{tests_section}
"""
        task_path.write_text(packet, encoding="utf-8")
        created += 1

    state = read_state()
    state["phase"] = "EXECUTING"
    state["last_updated"] = TODAY
    write_state(state)

    print(f"Created {created} task packet(s) in .forger/tasks/.")
    print("Phase set to EXECUTING.")

    # Print execution wave plan so PM/Orchestrator can see the dependency structure
    ready = get_ready_slices(slices)
    if ready:
        print(f"\nWave 1 (immediately executable): {', '.join(s['id'] for s in ready)}")

    # Warn if any task packets contain missing contracts
    missing_count = 0
    for s in slices:
        task_path = TASKS_DIR / f"{s['id']}.md"
        if task_path.exists() and "[Contract not found:" in task_path.read_text(encoding="utf-8"):
            print(f"  WARNING: {s['id']} has missing contract(s) -- DEV will be blocked.", file=sys.stderr)
            missing_count += 1
    if missing_count:
        print(f"  {missing_count} slice(s) have missing contracts. PM must create them before execution.", file=sys.stderr)


def cmd_execute(_: argparse.Namespace) -> None:
    """Show execution status and instructions for starting the Orchestrator."""
    state = read_state()
    if not state:
        print("No STATE.md found. Run 'init' first.")
        return

    phase = state.get("phase", "DISCOVERY")
    locked = state.get("locked", "false")

    if locked != "true":
        print("Project is not locked. Switch to 'forger-planner' mode and define your project first.")
        return

    if phase == "PLANNING":
        print("Phase: PLANNING -- PM agent is still generating planning documents.")
        print("Wait for the PM to complete, then come back.")
        return

    if phase != "EXECUTING":
        print(f"Phase: {phase}")
        print("Run 'orchestrate' to generate task packets and switch to EXECUTING phase.")
        return

    # Phase is EXECUTING
    if not SLICES_PATH.exists():
        print("SLICES.md not found. Run 'orchestrate' first.")
        return

    slices = load_slices()
    if not slices:
        print("No slices found. The PM agent must generate SLICES.md first.")
        return

    pending = [s for s in slices if s["status"] == "pending"]
    in_prog = [s for s in slices if s["status"] == "in_progress"]
    done = [s for s in slices if s["status"] == "done"]
    failed = [s for s in slices if s["status"] == "failed"]

    print(f"Phase:       EXECUTING")
    print(f"Slices:      {len(done)}/{len(slices)} done  |  {len(pending)} pending  |  {len(in_prog)} in-progress  |  {len(failed)} failed")

    if pending or in_prog:
        ready = get_ready_slices(slices)
        if ready:
            print(f"Ready now:   {', '.join(s['id'] for s in ready)}")
        print()
        print("To begin autonomous execution:")
        print("  Switch to 'forger-orchestrator' mode in Kilo Code.")
        print("  The Orchestrator will immediately begin executing all pending slices.")
    else:
        print()
        print("All slices are done or failed. Switch to 'forger-auditor' mode for the compliance audit.")


def cmd_next_slice(_: argparse.Namespace) -> None:
    if not SLICES_PATH.exists():
        print("NO_SLICES")
        return
    slices = load_slices()
    # Check in_progress slices FIRST -- they must be resolved before new work starts
    for s in slices:
        if s["status"] == "in_progress":
            done_file = TASKS_DIR / f"{s['id']}-DONE.md"
            if not done_file.exists():
                # STALE: session died before DEV finished
                print(f"STALE:{s['id']}: {s['title']}")
            else:
                # DEV done but session died before QA ran
                print(f"QA_PENDING:{s['id']}: {s['title']}")
            return
    # No in_progress slices -- find next pending
    for s in slices:
        if s["status"] == "pending":
            print(f"{s['id']}: {s['title']}")
            return
    print("ALL_DONE")


def cmd_next_wave(_: argparse.Namespace) -> None:
    """Print the current execution wave: all pending slices with satisfied dependencies.

    Output formats:
      WAVE:S3,S4,S7        -- comma-separated IDs of all currently unblocked slices
      ALL_DONE             -- all slices are done or failed
      BLOCKED:S5,S6        -- pending slices exist but all are blocked by unfinished dependencies
      STALE:[id]: [title]  -- in_progress slice with no DONE.md (session died mid-DEV)
      QA_PENDING:[id]: ... -- in_progress slice with DONE.md but no QA report
      NO_SLICES            -- SLICES.md not found or empty

    The Orchestrator uses this command instead of next-slice to enable wave-based
    execution: all slices in a WAVE can be processed simultaneously (when async
    new_task() is available) or in sequence (current default).
    """
    if not SLICES_PATH.exists():
        print("NO_SLICES")
        return
    slices = load_slices()
    if not slices:
        print("NO_SLICES")
        return

    # Resolve in_progress slices first (STALE/QA_PENDING must be handled before new work)
    for s in slices:
        if s["status"] == "in_progress":
            done_file = TASKS_DIR / f"{s['id']}-DONE.md"
            if not done_file.exists():
                print(f"STALE:{s['id']}: {s['title']}")
            else:
                print(f"QA_PENDING:{s['id']}: {s['title']}")
            return

    # Check if all work is done
    if all(s["status"] in ("done", "failed") for s in slices):
        print("ALL_DONE")
        return

    # Get all currently unblocked pending slices
    ready = get_ready_slices(slices)
    if not ready:
        # Pending slices exist but all are blocked (waiting on in_progress or unresolved deps)
        blocked_ids = [s["id"] for s in slices if s["status"] == "pending"]
        print(f"BLOCKED:{','.join(blocked_ids)}")
        return

    print("WAVE:" + ",".join(s["id"] for s in ready))


