def cmd_list_slices(_: argparse.Namespace) -> None:
    if not SLICES_PATH.exists():
        print("No SLICES.md found.")
        return
    slices = load_slices()
    if not slices:
        print("No slices found.")
        return

    STATUS_ICONS = {"pending": "[ ]", "in_progress": "[~]", "done": "[x]", "failed": "[!]"}
    id_width = max(len(s["id"]) for s in slices)

    # Group slices by burst, preserving insertion order
    burst_groups: dict[str, list[dict]] = {}
    no_burst: list[dict] = []
    for s in slices:
        burst = s.get("burst", "").strip()
        if burst:
            if burst not in burst_groups:
                burst_groups[burst] = []
            burst_groups[burst].append(s)
        else:
            no_burst.append(s)

    for burst_name, burst_slices in burst_groups.items():
        b_done = sum(1 for s in burst_slices if s["status"] == "done")
        b_total = len(burst_slices)
        print(f"\n{burst_name}  ({b_done}/{b_total})")
        for s in burst_slices:
            icon = STATUS_ICONS.get(s["status"], "[ ]")
            title = s["title"].split(" - ", 1)[-1] if " - " in s["title"] else s["title"]
            print(f"  {icon} {s['id'].ljust(id_width)}  {title}")

    if no_burst:
        print("\n(no burst)")
        for s in no_burst:
            icon = STATUS_ICONS.get(s["status"], "[ ]")
            title = s["title"].split(" - ", 1)[-1] if " - " in s["title"] else s["title"]
            print(f"  {icon} {s['id'].ljust(id_width)}  {title}")

    # Overall progress summary
    total = len(slices)
    done = sum(1 for s in slices if s["status"] == "done")
    failed = sum(1 for s in slices if s["status"] == "failed")
    in_prog = sum(1 for s in slices if s["status"] == "in_progress")
    pct = int(100 * done / total) if total else 0
    summary = f"\nProgress: {done}/{total} done ({pct}%)"
    if in_prog:
        summary += f"  |  {in_prog} in-progress"
    if failed:
        summary += f"  |  {failed} failed"
    print(summary)


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        description="Forger CLI -- deterministic markdown-driven workflow helper",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
Commands:
  init                    Init project in current directory (creates docs + Kilo Code modes)
  install                 Install Forger modes globally (~/.kilo/custom_modes.yaml)
  status                  Show current workflow state and slice progress
  lock                    Lock project scope (freeze PROJECT.md)
  unlock                  Unlock project scope
  set-interrupt MODE      Set execution interrupt mode (autonomous|per_milestone|per_burst|per_slice)
  reset                   Reset to PLANNING phase (wipe execution artifacts, keep PROJECT.md + contracts)
  plan                    Start planning phase (creates planning doc templates)
  orchestrate             Generate task packets from SLICES.md (validates DAG, detects cycles)
  execute                 Show execution status and how to start the Orchestrator
  next-slice              Print the next pending slice (STALE/QA_PENDING/ALL_DONE aware)
  next-wave               Print all currently unblocked slices for wave-based parallel execution
  start-slice ID          Mark a slice as in_progress
  complete-slice ID       Mark a slice as done
  fail-slice ID           Mark a slice as failed
  list-slices             List all slices grouped by burst with progress counters
""",
    )
    sub = parser.add_subparsers(dest="cmd", required=True)

    sub.add_parser("init", help="Init project in current directory").set_defaults(func=cmd_init)
    sub.add_parser("install", help="Install modes globally to ~/.kilo/custom_modes.yaml").set_defaults(func=cmd_install)
    sub.add_parser("status", help="Show current state and slice progress").set_defaults(func=cmd_status)
    sub.add_parser("lock", help="Lock project scope").set_defaults(func=cmd_lock)
    sub.add_parser("unlock", help="Unlock project scope").set_defaults(func=cmd_unlock)

    interrupt_p = sub.add_parser("set-interrupt", help="Set execution interrupt mode")
    interrupt_p.add_argument("mode", choices=["autonomous", "per_milestone", "per_burst", "per_slice"],
                             help="autonomous=no pauses, per_slice/per_burst/per_milestone=pause at boundaries")
    interrupt_p.set_defaults(func=cmd_set_interrupt)

    sub.add_parser("reset", help="Reset to PLANNING phase (wipe execution artifacts)").set_defaults(func=cmd_reset)
    sub.add_parser("plan", help="Start planning phase").set_defaults(func=cmd_plan)
    sub.add_parser("orchestrate", help="Generate task packets (validates DAG, detects cycles)").set_defaults(func=cmd_orchestrate)
