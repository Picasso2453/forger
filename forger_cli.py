def _find_forger_root() -> Path:
    """Locate the Forger data directory (prompts, custom_modes.yaml).

    Resolution order:
    1. pip-installed: importlib.resources finds forger_code/data/ in site-packages
    2. Source layout: forger_code/data/ next to this script (cloned repo, no install)
    3. Legacy fallback: script directory itself
    """
    try:
        from importlib.resources import files as _res_files
        p = Path(str(_res_files("forger_code").joinpath("data")))
        if (p / "prompts").exists():
            return p
    except Exception:
        pass
    script_dir = Path(__file__).resolve().parent
    src_data = script_dir / "forger_code" / "data"
    if src_data.exists():
        return src_data
    return script_dir


